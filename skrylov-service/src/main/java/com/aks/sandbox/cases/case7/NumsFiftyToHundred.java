package com.aks.sandbox.cases.case7;

public class NumsFiftyToHundred {

    public static void main(String[] args) {


        for (int j = 50; j <= 100; j++) {
            for (int i = 0; i <= 10; i++) {
                System.out.println(j);
            }
        }

    }

}
